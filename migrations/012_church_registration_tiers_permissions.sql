-- ============================================================================
-- SECTION 4: RLS on roles table
-- ============================================================================

ALTER TABLE public.roles ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.roles FORCE ROW LEVEL SECURITY;

-- Roles are a public reference table — any authenticated user can read them
CREATE POLICY "roles: select for authenticated users"
  ON public.roles
  FOR SELECT
  TO authenticated
  USING (true);

-- Only service role can modify roles (no INSERT/UPDATE/DELETE for authenticated)
-- This is enforced by the absence of INSERT/UPDATE/DELETE policies.


-- ============================================================================
-- SECTION 5: Create registration_keys table for pre-generated invite codes
-- ============================================================================

CREATE TABLE IF NOT EXISTS public.registration_keys (
  id          UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  key         TEXT UNIQUE NOT NULL,
  tier        TEXT NOT NULL DEFAULT 'standard'
              CHECK (tier IN ('standard', 'premium', 'enterprise')),
  claimed_by  UUID REFERENCES public.tenants(id) ON DELETE SET NULL,
  created_at  TIMESTAMPTZ NOT NULL DEFAULT now(),
  claimed_at  TIMESTAMPTZ
);

COMMENT ON TABLE public.registration_keys IS
  'Pre-generated registration keys that churches use for self-service signup. '
  'Each key maps to a tier and can only be claimed once.';

ALTER TABLE public.registration_keys ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.registration_keys FORCE ROW LEVEL SECURITY;

-- No authenticated user policies — registration keys are only accessed
-- by the service role during the registration flow.


-- ============================================================================
-- SECTION 6: Expand role CHECK constraint on tenant_memberships
-- ============================================================================

-- The existing CHECK constraint only allows 'admin', 'pastor', 'member'.
-- We need to add 'accountant' and 'worship_leader'.
ALTER TABLE public.tenant_memberships
  DROP CONSTRAINT IF EXISTS tenant_memberships_role_check;

ALTER TABLE public.tenant_memberships
  ADD CONSTRAINT tenant_memberships_role_check
  CHECK (role IN ('admin', 'pastor', 'accountant', 'worship_leader', 'member'));


