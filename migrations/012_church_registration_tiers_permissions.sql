-- =============================================================================
-- Migration 012: Church Registration, Tiers, and Granular Admin Permissions
--
-- Adds:
--   1. tier + registration_key columns to tenants (for self-service registration)
--   2. permissions JSONB column to tenant_memberships (granular sub-admin perms)
--   3. roles reference table with default role definitions and permissions
--   4. slug column on tenants (the "Church App ID" from registration)
--   5. RLS policies for the new roles table
--
-- Depends on: 001_initial_schema_and_rls.sql (tenants, tenant_memberships)
-- =============================================================================

-- ============================================================================
-- SECTION 1: Alter tenants table
-- ============================================================================

-- Tier determines the feature set available to the church
ALTER TABLE public.tenants
  ADD COLUMN IF NOT EXISTS tier TEXT NOT NULL DEFAULT 'standard'
    CHECK (tier IN ('standard', 'premium', 'enterprise'));

-- Registration key — unique invite code for self-service church registration.
-- Pre-generated by platform admins; consumed during POST /tenants/register.
ALTER TABLE public.tenants
  ADD COLUMN IF NOT EXISTS registration_key TEXT UNIQUE;

-- Slug — the human-readable "Church App ID" (e.g., "grace-church").
-- Used in URLs and deep links. Must be unique and lowercase alphanumeric + dashes.
ALTER TABLE public.tenants
  ADD COLUMN IF NOT EXISTS slug TEXT UNIQUE
    CHECK (slug ~ '^[a-z0-9][a-z0-9-]*[a-z0-9]$' OR slug ~ '^[a-z0-9]$');

COMMENT ON COLUMN public.tenants.tier IS
  'Subscription tier: standard (free), premium, enterprise. Controls feature gates.';
COMMENT ON COLUMN public.tenants.registration_key IS
  'One-time invite code consumed during church self-registration. NULL after use or if created by super admin.';
COMMENT ON COLUMN public.tenants.slug IS
  'URL-friendly church identifier (e.g., grace-church). Set during registration.';


-- ============================================================================
-- SECTION 2: Alter tenant_memberships table
-- ============================================================================

-- Granular permissions JSONB — allows sub-admin roles (accountant, worship leader)
-- to have specific capabilities beyond the base role.
ALTER TABLE public.tenant_memberships
  ADD COLUMN IF NOT EXISTS permissions JSONB NOT NULL DEFAULT '{}';

COMMENT ON COLUMN public.tenant_memberships.permissions IS
  'Granular permission overrides as JSONB. Keys: manage_finance, manage_content, manage_members, manage_worship, view_analytics. Values: true/false.';


-- ============================================================================
-- SECTION 3: Create roles reference table
-- ============================================================================

CREATE TABLE IF NOT EXISTS public.roles (
  id    UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  name  TEXT UNIQUE NOT NULL,
  permissions JSONB NOT NULL DEFAULT '{}'
);

COMMENT ON TABLE public.roles IS
  'Reference table defining standard roles and their default permission sets. '
  'Used as a template when assigning roles — actual permissions are stored on tenant_memberships.';

